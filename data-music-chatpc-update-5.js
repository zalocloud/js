function generateMusicHTML(musicData, containerId = "music-container") {
  const container = document.getElementById(containerId);

  musicData.forEach(song => {
    const songElement = document.createElement("div");
    songElement.classList.add("ghichu-music");

    // Create the image
    const imgElement = document.createElement("img");
    imgElement.src = song.image;
    imgElement.classList.add("logo");
    imgElement.alt = "Logo";

    // Create the content section
    const contentDiv = document.createElement("div");
    contentDiv.classList.add("container-music");

    const content = `
      <div class="content">
        <div class="phantren">${song.title}</div>
        <div class="phanduoi">
          <div class="container">
            <div class="scrollingText" data-speed="0">
              <span class="name-singer">${song.singer}</span>
              <span class="time-musice"></span>
            </div>
          </div>
        </div>
      </div>
    `;

    contentDiv.innerHTML = content;

    // Create the play-download section
    const playDownloadDiv = document.createElement("div");
    playDownloadDiv.classList.add("play-download");

    const downloadMusicDiv = `
      <div class="download-music">
        <div class="download-button">
          <a href="${song.mp3Url}" download="${song.downloadName}" class="btn-download">
            <i class="fas fa-download"></i>
          </a>
        </div>
      </div>
    `;

    const playerMusicDiv = `
      <div class="player-music">
        <div class="mediPlayer">
          <audio class="listen" preload="none" data-size="30" src="${song.mp3Url}"></audio>
        </div>
      </div>
    `;

    playDownloadDiv.innerHTML = downloadMusicDiv + playerMusicDiv;

    // Append all elements to the song container
    songElement.appendChild(imgElement);
    songElement.appendChild(contentDiv);
    songElement.appendChild(playDownloadDiv);

    // Append the song element to the main container
    container.appendChild(songElement);
  });
}

// Call the function to generate HTML for both containers
generateMusicHTML(musicData, "music-container");
